const dropzone = document.getElementById('dropzone');
const fileInput = document.getElementById('fileInput');
const previewArea = document.getElementById('previewArea');
const previewMedia = document.getElementById('previewMedia');
const fileNameDisplay = document.getElementById('fileNameDisplay');
let selectedFile = null;

// File Handling
['dragenter', 'dragover', 'dragleave', 'drop'].forEach(e => {
    dropzone.addEventListener(e, (evt) => {
        evt.preventDefault();
        evt.stopPropagation();
    });
});

dropzone.addEventListener('dragover', () => dropzone.classList.add('dragover'));
dropzone.addEventListener('dragleave', () => dropzone.classList.remove('dragover'));
dropzone.addEventListener('drop', (e) => {
    dropzone.classList.remove('dragover');
    handleFiles(e.dataTransfer.files);
});

fileInput.addEventListener('change', (e) => handleFiles(e.target.files));

function handleFiles(files) {
    if (files.length > 0) {
        selectedFile = files[0];
        displayPreview(selectedFile);
        
        // Auto-start if a scan type is already selected
        if (document.getElementById('scanType').value) {
            setTimeout(() => document.getElementById('analyzeBtn').click(), 300);
        }
    }
}

function displayPreview(file) {
    fileNameDisplay.textContent = file.name;
    previewArea.classList.remove('d-none');
    previewMedia.innerHTML = '';
    
    if (file.type.startsWith('image/')) {
        const img = document.createElement('img');
        const reader = new FileReader();
        reader.onload = (e) => {
            img.src = e.target.result;
            window.lastUploadedFileDataUrl = e.target.result;
        };
        reader.readAsDataURL(file);
        previewMedia.appendChild(img);
    } else if (file.type === 'application/pdf') {
        const iframe = document.createElement('iframe');
        iframe.src = URL.createObjectURL(file);
        previewMedia.appendChild(iframe);
        window.lastUploadedFileDataUrl = null;
    }
}

window.clearPreview = function() {
    selectedFile = null;
    fileInput.value = '';
    window.lastUploadedFileDataUrl = null;
    previewArea.classList.add('d-none');
    document.getElementById('resultArea').classList.add('d-none');
};

