function parseAIResult(raw, activeLang) {
    const lang = activeLang || localStorage.getItem('mediai_lang') || 'en';
    if (!raw) return { diagnosis: 'Unknown', confidence: '0%', recommendations: 'None', boneName: '', fractureType: '', severity: '', medicalReport: '', treatmentPlan: '', heatmap: '', _lang: lang };
    
    try {
        const parsed = JSON.parse(raw);
        
        // Handle bilingual format returned by updated models
        if (parsed._isBilingual) {
            return {
                diagnosis: lang === 'ar' ? (parsed.DiagnosisAr || parsed.Diagnosis) : (parsed.DiagnosisEn || parsed.Diagnosis),
                confidence: parsed.Confidence || '90%',
                recommendations: lang === 'ar' ? (parsed.RecommendationsAr || parsed.Recommendations) : (parsed.RecommendationsEn || parsed.Recommendations),
                boneName: lang === 'ar' ? (parsed.BoneNameAr || parsed.BoneName || '') : (parsed.BoneNameEn || parsed.BoneName || ''),
                fractureType: parsed.FractureType || parsed.fracture_type || '',
                severity: lang === 'ar' ? (parsed.SeverityAr || parsed.Severity) : (parsed.SeverityEn || parsed.Severity),
                medicalReport: lang === 'ar' ? (parsed.MedicalReportAr || parsed.MedicalReport) : (parsed.MedicalReportEn || parsed.MedicalReport),
                treatmentPlan: lang === 'ar' ? (parsed.TreatmentPlanAr || parsed.TreatmentPlan) : (parsed.TreatmentPlanEn || parsed.TreatmentPlan),
                heatmap: parsed.Heatmap || parsed.heatmap || '',
                _error_message: parsed._error_message || '',
                _lang: lang
            };
        }

        // Standard parsing fallback
        let storedLang = parsed._lang || parsed._Language || '';
        if (!storedLang) {
            // Auto-detect from content
            const allText = (parsed.Diagnosis || parsed.diagnosis || '') +
                            (parsed.MedicalReport || parsed.medicalReport || '') +
                            (parsed.Recommendations || parsed.recommendations || '');
            storedLang = (/[\u0600-\u06FF]/).test(allText) ? 'ar' : 'en';
        }
        return {
            diagnosis: parsed.Diagnosis || parsed.diagnosis || 'Unknown',
            confidence: parsed.Confidence || parsed.confidence || '90%',
            recommendations: parsed.Recommendations || parsed.recommendations || '',
            boneName: parsed.BoneName || parsed.bone_name || '',
            fractureType: parsed.FractureType || parsed.fracture_type || '',
            severity: parsed.Severity || parsed.severity || '',
            medicalReport: parsed.MedicalReport || parsed.report || parsed.medicalReport || '',
            treatmentPlan: parsed.TreatmentPlan || parsed.treatment_plan || parsed.treatmentPlan || '',
            heatmap: parsed.Heatmap || parsed.heatmap || '',
            _error_message: parsed._error_message || '',
            _lang: storedLang
        };
    } catch (e) {
        const hasArabic = (/[\u0600-\u06FF]/).test(raw);
        return {
            diagnosis: raw,
            confidence: '90%',
            recommendations: '',
            boneName: '',
            fractureType: '',
            severity: '',
            medicalReport: '',
            treatmentPlan: '',
            heatmap: '',
            _lang: hasArabic ? 'ar' : 'en'
        };
    }
}

