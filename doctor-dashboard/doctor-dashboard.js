// ─── Specialty Translation Map ────────────────────────────────────────────────
const specialtyMap = {
    en_to_ar: {
        'pneumonia':  'أشعة الصدر (الالتهاب الرئوي)',
        'orthopedics': 'جراحة العظام',
        'neurology':   'المخ والأعصاب',
    },
    ar_to_en: {
        'أشعة الصدر (الالتهاب الرئوي)':  'Pneumonia',
        'جراحة العظام': 'Orthopedics',
        'المخ والأعصاب': 'Neurology',
    }
};

function translateSpecialty(specialty, lang) {
    if (!specialty) return '';
    const lower = specialty.trim().toLowerCase();
    if (lang === 'ar') {
        // Try en→ar first
        if (specialtyMap.en_to_ar[lower]) return specialtyMap.en_to_ar[lower];
        // Already Arabic? return as-is
        return specialty;
    } else {
        // Try ar→en
        if (specialtyMap.ar_to_en[specialty.trim()]) return specialtyMap.ar_to_en[specialty.trim()];
        // Capitalise English if already English
        return specialty.charAt(0).toUpperCase() + specialty.slice(1);
    }
}

function formatDoctorName(rawName, lang) {
    if (!rawName) return '';
    // Strip any existing prefix
    let base = rawName
        .replace(/^د\.\s*/u, '')
        .replace(/^Dr\.\s*/i, '')
        .trim();
    return lang === 'ar' ? `د. ${base}` : `Dr. ${base}`;
}

function refreshSidebarProfile() {
    const lang       = localStorage.getItem('mediai_lang') || 'en';
    const rawName    = localStorage.getItem('mediai_user_specialty_raw_name') ||
                       localStorage.getItem('mediai_user_name') || '';
    const rawSpec    = localStorage.getItem('mediai_user_specialty') || '';

    const sidebarNameEl  = document.getElementById('sidebarDoctorName');
    const specialtyEl    = document.getElementById('sidebarDoctorSpecialty');
    const specialtyDot   = document.getElementById('sidebarSpecialtyDot');
    const dashNameEl     = document.getElementById('dashDoctorName');

    const displayName = translateDoctorName(rawName, lang);
    if (sidebarNameEl && rawName) {
        sidebarNameEl.textContent = formatDoctorName(displayName, lang);
    }
    if (dashNameEl && rawName) {
        const base = displayName.replace(/^د\.\s*/u, '').replace(/^Dr\.\s*/i, '').trim();
        dashNameEl.textContent = base.split(' ')[0];
    }
    if (specialtyEl) {
        const translated = translateSpecialty(rawSpec, lang);
        specialtyEl.textContent = translated;
        if (specialtyDot) specialtyDot.style.display = translated ? 'inline' : 'none';
    }

    // Show clinic address from localStorage
    const sidebarAddr = document.getElementById('sidebarClinicAddress');
    const savedAddr = localStorage.getItem('mediai_user_address');
    if (sidebarAddr && savedAddr) {
        sidebarAddr.innerHTML = window.toMapLink ? window.toMapLink(savedAddr) : `<i class="bi bi-geo-alt"></i> <span>${savedAddr}</span>`;
        sidebarAddr.style.display = '';
    } else if (sidebarAddr) {
        sidebarAddr.style.display = 'none';
    }
}

function parseAIResult(raw) {
    if (!raw) return 'Pending';
    try {
        const parsed = JSON.parse(raw);
        return parsed.Diagnosis || parsed.diagnosis || raw;
    } catch (e) {
        return raw;
    }
}

