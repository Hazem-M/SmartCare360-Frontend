function renderMiniCalendar() {
    const calendarContainer = document.getElementById('miniCalendarBody');
    if (!calendarContainer) return;
    
    const lang = localStorage.getItem('mediai_lang') || 'en';
    const now = new Date();
    const currentYear = now.getFullYear();
    const currentMonth = now.getMonth();
    const today = now.getDate();
    
    // First day of the month
    const firstDay = new Date(currentYear, currentMonth, 1).getDay();
    // Number of days in the month
    const totalDays = new Date(currentYear, currentMonth + 1, 0).getDate();
    
    const monthNamesEn = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];
    const monthNamesAr = ['يناير', 'فبراير', 'مارس', 'أبريل', 'مايو', 'يونيو', 'يوليو', 'أغسطس', 'سبتمبر', 'أكتوبر', 'نوفمبر', 'ديسمبر'];
    
    const monthTitle = document.getElementById('miniCalendarTitle');
    if (monthTitle) {
        monthTitle.textContent = (lang === 'ar' ? monthNamesAr[currentMonth] : monthNamesEn[currentMonth]) + ' ' + currentYear;
    }
    
    let html = '';
    // Weekday headers
    const daysHeadersEn = ['Su', 'Mo', 'Tu', 'We', 'Th', 'Fr', 'Sa'];
    const daysHeadersAr = ['أح', 'ن', 'ث', 'ر', 'خ', 'ج', 'س'];
    const headers = lang === 'ar' ? daysHeadersAr : daysHeadersEn;
    
    html += '<div class="calendar-grid-header">';
    headers.forEach(h => {
        html += `<div class="calendar-cell-header">${h}</div>`;
    });
    html += '</div>';
    
    html += '<div class="calendar-grid-body">';
    // Blank cells before the first day
    for (let i = 0; i < firstDay; i++) {
        html += '<div class="calendar-cell empty"></div>';
    }
    
    // Day cells
    for (let day = 1; day <= totalDays; day++) {
        const isToday = day === today;
        const activeClass = isToday ? 'today-highlight' : '';
        html += `<div class="calendar-cell ${activeClass}">${day}</div>`;
    }
    html += '</div>';
    
    calendarContainer.innerHTML = html;
}

