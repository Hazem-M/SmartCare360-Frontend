function renderPatients(filter) {
    const lang = localStorage.getItem('mediai_lang') || 'en';
    const tbody = document.getElementById('patientsListTableBody');
    if (!tbody) return;
    const allPatients = [];
    const uniqueIds = [];
    (cachedAppointments || []).forEach(a => {
        const pid = a.patientId || a.PatientId;
        if (pid && !uniqueIds.includes(pid)) {
            uniqueIds.push(pid);
            allPatients.push({ id: pid, name: a.patientName || a.PatientName || 'Patient' });
        }
    });
    if (allPatients.length === 0 && window._cachedPastAppts) {
        (window._cachedPastAppts || []).forEach(a => {
            const pid = a.patientId || a.PatientId;
            if (pid && !uniqueIds.includes(pid)) {
                uniqueIds.push(pid);
                allPatients.push({ id: pid, name: a.patientName || a.PatientName || 'Patient' });
            }
        });
    }
    const filtered = filter ? allPatients.filter(p => p.name.toLowerCase().includes(filter.toLowerCase())) : allPatients;
    if (filtered.length === 0) {
        tbody.innerHTML = `<tr><td colspan="3" class="text-center py-4 text-muted">${lang === 'ar' ? 'لا توجد نتائج' : 'No results found'}</td></tr>`;
        return;
    }
    tbody.innerHTML = filtered.map(p => `
        <tr>
            <td class="fw-bold pine-slate-text">${p.name}</td>
            <td><span class="badge bg-success-subtle text-success px-2 py-1 rounded-pill">${lang === 'ar' ? 'نشط' : 'Active'}</span></td>
            <td><button class="btn btn-sm btn-outline-primary rounded-pill px-3" onclick="showRealPatientDetails('${p.id}')">${lang === 'ar' ? 'عرض التفاصيل' : 'View Details'}</button></td>
        </tr>
    `).join('');
}

async function autoCompletePastAppointments() {
    const lang = localStorage.getItem('mediai_lang') || 'en';
    if (!cachedAppointments || cachedAppointments.length === 0) return;
    const now = new Date();
    const pastPending = cachedAppointments.filter(a => {
        const d = new Date(a.appointmentDate || a.AppointmentDate);
        const status = a.status || a.Status || 'Scheduled';
        return d < now && d.toDateString() !== now.toDateString() && (status === 'Scheduled' || status === 'Confirmed');
    });
    for (const a of pastPending) {
        try {
            const id = a.id || a.Id || a.appointmentId || a.AppointmentId;
            if (id) {
                await Api.appointments.updateStatus(id, 'Completed');
            }
        } catch (_) {}
    }
    if (pastPending.length > 0) {
        const msg = lang === 'ar'
            ? `تم إكمال ${pastPending.length} موعد/مواعيد سابقة تلقائياً`
            : `Auto-completed ${pastPending.length} past appointment(s)`;
        showAlert(msg, 'info');
        loadDashboard();
    }
}

