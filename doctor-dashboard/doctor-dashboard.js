let chatContacts = [];
let currentChatId = null;
let chatConnection = null;

async function startSignalR() {
    const token = localStorage.getItem('mediai_token');
    if (!token) return;

    chatConnection = new signalR.HubConnectionBuilder()
        .withUrl("/chatHub", {
            accessTokenFactory: () => token
        })
        .withAutomaticReconnect()
        .build();

    chatConnection.on("ReceiveMessage", (data) => {
        console.log("Real-time message received:", data);
        if (currentChatId && String(data.senderId).toLowerCase() === String(currentChatId).toLowerCase()) {
            appendMessage('received', data.content);
        } else {
            const contact = chatContacts.find(c => String(c.id).toLowerCase() === String(data.senderId).toLowerCase());
            if (contact) {
                const lang = localStorage.getItem('mediai_lang') || 'en';
                showAlert(`${translations[lang]['new_msg_from'] || 'New message from'} ${contact.name}`, 'info');
            }
        }
    });

    try {
        await chatConnection.start();
        console.log("SignalR Connected.");
    } catch (err) {
        console.error("SignalR Connection Error:", err);
    }
}

async function initChatView() {
    startSignalR();

    // Bind Chat Input Form Submission
    const chatForm = document.getElementById('chatForm');
    if (chatForm) {
        chatForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            const input = document.getElementById('chatInput');
            const message = input.value.trim();
            if (!message || !currentChatId) return;

            appendMessage('sent', message);
            input.value = '';

            try {
                await Api.chat.send({ receiverId: currentChatId, content: message });
            } catch (err) {
                console.warn('Failed to send message:', err);
            }
        });
    }

    // Load contacts from API
    try {
        const data = await Api.chat.getContacts();
        const apiContacts = Array.isArray(data) ? data : (data.contacts || []);
        
        const mappedContacts = apiContacts.map(c => {
            const isDoc = (c.role || '').toLowerCase() === 'doctor';
            return {
                id: c.id || c._id,
                name: c.fullName || c.name || (isDoc ? 'Doctor' : 'Patient'),
                type: isDoc ? 'doctor' : 'patient',
                online: c.online ?? false,
                spec: c.specialty || c.specialization || (isDoc ? '' : 'Patient')
            };
        });

        // Hide AI bot since doctors are communicating with patients directly
        chatContacts = mappedContacts;
        if (mappedContacts.length > 0) {
            currentChatId = mappedContacts[0].id;
        } else {
            currentChatId = null;
        }
    } catch (e) {
        console.warn('Could not load contacts:', e);
    }

    renderContacts();
    setupChatFromUrl();
}

