// ═══════════════════════
async function loadPublicStats() {
    try {
        const stats = await Api.public.getStats();
        // Update data-counter attributes for animation
        const patientCounter = document.querySelector('[data-lang="trust_patients"]')?.previousElementSibling?.previousElementSibling;
        const accuracyCounter = document.querySelector('[data-lang="trust_accuracy"]')?.previousElementSibling?.previousElementSibling;
        const doctorCounter = document.querySelector('[data-lang="trust_doctors"]')?.previousElementSibling?.previousElementSibling;

        if (patientCounter) patientCounter.dataset.counter = stats.totalPatients || 12000;
        if (accuracyCounter) accuracyCounter.dataset.counter = parseInt(stats.accuracyStat) || 98;
        if (doctorCounter) doctorCounter.dataset.counter = stats.totalDoctors || 150;
    } catch (e) {
        console.warn('Could not load real stats:', e);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    // 1. IMPROVE: Show all content immediately (Fail-safe)
    document.querySelectorAll('.reveal-up, .reveal-left, .reveal-right').forEach(el => {
        el.classList.add('visible');
        el.style.opacity = '1';
    });
    
    initScrollReveal();
    loadPublicStats(); // DO NOT await here (Background load)
    initCounters();
});
